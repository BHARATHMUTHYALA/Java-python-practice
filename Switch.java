class Switch{
    public static void main(String[] args){
        String day ="Kojja";

        switch(day){
            case "SUNDAY":
            System.out.println("IT IS A HOLIDAY");
            break;
            case "MONDAY":
            System.out.println("IT IS MONDAY");
            break;
            case "TUESDAY":
            System.out.println("IT IS A TUESDAY");
            break;
            case "WEDNESDAY":
            System.out.println("IT IS A WEDNESDAY");
            break;
            case "THURSDAY":
            System.out.println("IT IS A THURSDAY");
            break;
            case "Friday":
            System.out.println("IT IS A FRIDAY");
            break;
            case "SATURDAY":
            System.out.println("IT IS A SATURDAY");
            break;
            default:
                 System.out.print("Not a good match");  
        }

    }
}
